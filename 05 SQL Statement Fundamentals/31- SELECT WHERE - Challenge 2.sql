-- A customer wants to know what the movie
-- "Outlaw Hanky" is about.
-- Could you give them the description for the 
-- movie "Outlaw Hankie"?

SELECT description
FROM film
WHERE title = 'Outlaw Hanky';

-- Answer:
-- A Thoughtful Story of a Astronaut And
-- a Composer who must Conquer a Dog in 
-- the Sahara Desert
